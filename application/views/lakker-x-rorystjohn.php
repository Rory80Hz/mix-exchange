				<ul class="answers">

					<li>
						<p><b>Rory St. John:</b> <span class="question"> You have been working together for many years now, from a band to breakcore, to now Lakker. How has the transition been over the years? Did it all happen naturally?</span></p>
						<p><b>Lakker:</b> It's been really easy and really natural. There's never been any sort of difficult phase. We've kind of always made what we want, when we wanted to, and then moved on if / when we got bored of a particular sound. There was never an urge or a feeling that we 'should' do a certain type of music. There was one stage, around about 2009, where we sat down and had a serious talk about where we wanted to go. Kind of like you do after a few years of being in a relationship. You need to figure out if it really has a future and what both parties want from it. We had gotten bored with a lot of the music we had been making, so we had to think about what we wanted to do next. Luckily we were both still on the same page musically, so we drew up a little manifesto that reinvigorated us and helped us move to where we are now.</p>
					</li>

					<li>
						<p><b>Rory St. John:</b> <span class="question"> What has been your biggest influence over the years?</span></p>
						<p><b>Lakker:</b> Hard to say. But probably each other. We've learned a lot from each other at various times over the years. And I think we still do. We both still surprise each other in the studio, which is great. Also, the people immediately around us - friends and family. You can't help but be influenced positively and negatively by the people closest you.</p>

					</li>

					<li>
						<p><b>Rory St. John:</b> <span class="question"> What one thing is a must on your musical Bucket List?</span></p>
						<p><b>Lakker:</b> Hmm. A headline slot at one of the big electronic music festivals - Sonar, Bloc, Movement...the kind of festivals we loved going to when we were younger. They were always super exciting and to play one would be a little dream come true.</p>
					</li>

					<li>
						<p><b>Rory St. John:</b> <span class="question"> Both of you have separate solo projects. What's the process for differentiating between what constitutes a Lakker track or an Arad / Eomac track?</span></p>

						<p><b>Lakker:</b> Ian - it kind of intuitively works itself out. Usually a certain type of sound will tell me if something is better for Eomac or Lakker. Sometimes I'll have an idea and really want to hear Dara's input, so it becomes Lakker. Sometimes I have an idea and I know exactly what I want for it and I see it through to completion myself so it's Eomac. I usually just know as a track develops though, which it's better suited to. I don't like to think about it too much.</p>

						<p>Dara -  Yeah not really sure , think it will become more apparent over time , I tend to write with out any project in mind and then figure out what i,ll do with it after, some track it becomes apparent straight away some get a bit of molding.</p>
					</li>

					<li>
						<p><b>Rory St. John:</b> <span class="question"> Favourite electronic Album of all time?</span></p>

						<p><b>Lakker:</b> Ian - The toughest question, always. My favourite is always something with real emotion for me. Off the top of my head 'Untrue' by Burial.</p>

						<p>Dara -  Aphex Twin Richard D James</p>
					</li>

				</ul>

