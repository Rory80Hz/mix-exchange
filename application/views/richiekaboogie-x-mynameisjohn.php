			<div class="col-70 r">
				<h3 id="meta-desc">The Mix Exchange welcomes two of Irelands finest producers/dj’s making noise at the moment. Both Richie and John have a sturdy history behind the turntables and are excellent examples of how their Djing ears can translate to the producing game.</h3>
				
				<h3>Very much in two halves, this exchange showcases exactly the direction these two lads are headed. Richie’s tone is shaped by his background in all things bass with his orbit tilted toward the gritty end of techno. John bring us a Dj set with a grimy hip hop pill laced with his own productions. </h3>

				<h3>As usual a chat ensues with breakfast, the democratisation of the production process, ambitions of having a coffee butler while hanging with Kendrick Lamar and being “sound” are the order of todays debate.</h3>
				
				<ul class="answers">
				
					<li>
						<p><b>mynameisjOHn:</b> <span class="question">Lets get right down to business. Where do you think our consciousness comes from?</span></p>
						<p><b>Richie K:</b>  hmmmm - somewhere in bulgaria</p>
					</li>
					
					<li>
						<p><b>mynameisjOHn:</b> <span class="question">Breakfast is an essential element to any day.  I take it very serious.  What does a Richie K breakfast look like?</span></p>
						<p><b>Richie K:</b>  oooh good one - tbh it depends on what im at that day....i work part-time - so work days - a banana w. yoghurt, 2 slices of brown bread with butter/cheese/jam and always coffee and a cigarette. Staples.</p>
					</li>
					
					<li>
						<p><b>mynameisjOHn:</b> <span class="question">The Wire vs Breaking Bad.  Which one would you choose and why? </span></p>
						<p><b>Richie K:</b> another brainsquasher....im gonna say Breaking Bad cuz of the thundering focus of the narrative - i think i prefer that than the Wire's multi-persepctive layered approach - although thats what is great about the Wire....but yeah the anticipation building towards that finale (in Breaking Bad) was some good TV.</p>
					</li>
					
					<li class="quote">
						“The backlash against EDM, the fragmentation of genres and the democratisation of the production process is causing some interesting shit to pop up all over the place.”
					</li>
					
					<li>
						<p><b>mynameisjOHn:</b> <span class="question"> I got to hear some of your collaborative tracks with the vocalist Joni earlier this year, and they're amazing.  Whats the plan with this project - releases, live, etc?</span></p>
						<p><b>Richie K:</b> Joni has a release of 2 of her tracks that i produced plus an Eomac remix of one of them coming early in the New Year - so we are getting a live set together at the moment. She is immensely talented so its a pleasure working with her.</p>
					</li>
					
					<li>
						<p><b>mynameisjOHn:</b> <span class="question"> If you could sit down and talk shop with any musician dead or alive, who would you choose to break bread with?</span></p>
						<p><b>Richie K:</b> Sheeeesh kebabs...if there was a jobbridge internship going with King Tubby i think id jack in my job and dedicate myself to his service.  but in terms of actually breaking bread maybe Action Bronson cuz he would probably be breaking some sexy bread..</p>
					</li>
					
					<li>
						<p><b>mynameisjOHn:</b> <span class="question">Where do you see music going in the next ten years?  Do you think its headed in a positive direction?</span></p>
						<p><b>Richie K:</b> Ha. looking into my crystal ball i foresee further cyclical patterns continuing in terms of popular electronic music fashions....maybe proper electro needs its time in the sun again. The backlash against EDM, the fragmentation of genres and the democratisation of the production process is causing some interesting shit to pop up all over the place. So maybe a new IDM type force will emerge that will incorporate more noise/interesting sound design with hybridised dancing rhythms. So South African IDM is where its gonna be at essentially ;P 
I’m excited with things right now though  :)</p>
					</li>
										
				</ul>
			
