				<h3 id="meta-desc">Dara Smith, Ian McDonnell and <a href="http://www.rorystjohn.com/">Rory St. John</a> are three originators of euphonious sounds that emerged from Dublin’s electronic music scene, and migrated with ease to the musical hub of Berlin.</h3>

				<h3>Nowadays, the Berlin dwelling duo of <a href="http://www.randsrecords.com/artists/lakker">Lakker</a>, is a different one to that which mangled venues with breakcore sets in their home town in the past, whilst Rory’s UniNerves off-shoot has given him focus on his back-to-basics techno project. </h3>

				<h3>Although they have migrated both physically and musically from their beginnings, their roots are still present in their work today. Evidence of this is audible throughout this excellent instalment of the mix exchange.</h3>

				<ul class="answers">

					<li>
						<p><b>Lakker:</b> <span class="question"> What was one memorable track / experience / gig that made you want to make music?</span></p>
						<p><b>Rory St. John:</b> My ear was always tuned electronic sounds I think, so it's hard to pick one track, but as an experience, this one is one that had the most effect on me:</p>

						<p>I was already making music by this point (albeit very bad) but a friend and I got kicked out of a 'normal' club one Saturday, when I was about 18, and we ended up going into Switch on Eustace St. Dublin. </p>

						<p>Barry Dempsey and Dean Sherry were belting out some proper techno there, my jaw hit the ground, and I was forever changed after that :) </p>
					</li>

					<li>
						<p><b>Lakker:</b> <span class="question"> And why did that affect you?</span></p>
						<p><b>Rory St. John:</b> Up until this point I didn't really know what good techno was. I had some odd bits that I found in one of the more commercial record shops, but wasnt aware of Spindizzy and only had been into Tag a few times. </p>
						<p>When the Phunk'd Up lads played, they would start with some housier stuff, then into full on stompers.</p>
						<p>For a while after that I was in Switch 4 times a week.... Joe McGrath gave my first proper club gig at Damage where him, Fran Hartnett, Sunil Sharpe, Giles Armstrong and Jamie Behan showed me what proper techno was all about. </p>
					</li>

					<li>
						<p><b>Lakker:</b> <span class="question"> Name a record people may be surprised you're really into.</span></p>

						<p><b>Rory St. John:</b> Ultravox - Vienna</p>
					</li>
					
					<li>
						<p><b>Lakker:</b> <span class="question"> What's your favourite current track?</span></p>

