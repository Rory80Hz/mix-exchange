				<ul class="answers">
				
					<li>
						<p><b>Richie K:</b> <span class="question">Production-wise what's your set-up?</span></p>
						<p><b>mynameisjOHn:</b> Nothing too fancy really.  Turntables, mixer, records, macbook, Logic, pad controller.  COFFEE.  Whatever is at hand really.  Im less concerned with equipment and software, and more focused on getting the ol' melon in the right place to get something creative done.</p>
					</li>
					
					<li>
						<p><b>Richie K:</b> <span class="question">which do u prefer - DJ-ing or live shows?</span></p>
						<p><b>mynameisjOHn:</b> Tough one.  I love them both.  The live-show is obviously good for the extra energy and the potential to go topless and jump around like a loon, but then its a fully rehearsed/concrete show with little room for mistakes or improvisation. </p>
						<p>With DJing, there's more scope to go in different directions and challenge people a bit more.  I think I've dedicated a lot of time to the live-thing over the last year, and sometimes i forget that DJing is still even an option.  It is though. </p>
					</li>
					
					<li>
						<p><b>Richie K:</b> <span class="question"> Limerick, from a distance, seems like its hopping with a really interesting music scene - whats behind that?</span></p>
						<p><b>mynameisjOHn:</b> Good coffee and bad moods.  Appearances can be deceptive.  I wouldn't say its hopping, but its definitely headed in the right direction.  The city needs more venues, but its good to see that the youth are down to support whats going on. </p>
					</li>
					
					<li>
						<p><b>Richie K:</b> <span class="question"> Whats your favourite record of 2014?</span></p>
						<p><b>mynameisjOHn:</b> Clap Clap - 'Tayi Bebba'.  Its a record by an Italian producer who goes deep into the concept of mixing african rhythms with footwork.  Been listening to a lot of Togo/Benin/Ghana music lately, so the Clap Clap stuff really resonated with where my musical tastes are right now. </p>
					</li>
					
					<li>
						<p><b>Richie K:</b> <span class="question"> The live show with godknows is HYPE - whats the plans with that project?</span></p>
						<p><b>mynameisjOHn:</b> Yup, we're now a three-piece.  We've brought Murli into the fold, and we're about to amalgamate everything together to just be Rusangano Family.  Murli has a little EP on the way that really shows how lyrical and thoughtful he is as a person and an MC.  Once that comes out, we want to go full throttle on this beast, and end up recording an album in LA with Kendrick Lamar and BadBadNotGood in a massive gaff with a swimming pool and my own personal butler who just follows me around and pours fresh coffee into my mug every time I blink. </p>
					</li>
					
					<li>
						<p><b>Richie K:</b> <span class="question"> What equipment do you use, and do you have a method to your production?</span></p>
						<p><b>mynameisjOHn:</b> Pretty simple, PC, little mixer, spring reverb, analog delay, percussion, and little synth. I need more synths by the way, donations welcome, don't hesitate to send a LOT of Money. ;)</p>
					</li>
					
					<li>
						<p><b>Richie K:</b> <span class="question"> You can hear the influence of grime, footwork and contemporary hiphop in your beats - if you had to favourite one of them genres which would it be and why? </span></p>
						<p><b>mynameisjOHn:</b> Nope.  I just like music, man.  I think when you come from a hiphop background, you just tend to see all styles of music as something you can eff with. </p>
					</li>
					
					<li>
						<p><b>Richie K:</b> <span class="question"> I reckon the young people of limerick and clare are pretty darn lucky to have yourself involved in music/production youth work - any hot tips re. upcoming MCs or producers from that world?</span></p>
						<p><b>mynameisjOHn:</b> Yeah, stop downloading shite quality beats off Youtube and rapping over them.  Learn your history.  Learn your trade.  Have fun with it.  Don't take yourself TOO serious, but DO take your self serious.  Push yourself to take a different road than everyone else.   Buy music, support music, look at art, appreciate culture, be sound. </p>
					</li>
										
				</ul>
				
